import mongoose from 'mongoose';

const ComponentSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        unique: true
    },
    category: {
        type: String,
        required: true,
    },
    price: {
        type: Number,
        required: true,
    },
    brand: {
        type: String,
        required: true,
    }
}, {timestamps: true});

export const Component = mongoose.model('Component', ComponentSchema);